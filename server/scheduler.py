# Scheduler thread
# ---------------------------------------------------------------------------


def _today_iso(now_utc: datetime) -> str:
    return now_utc.date().isoformat()


class BackgroundScheduler:
    """One worker thread, evaluated every minute."""

    def __init__(
        self,
        *,
        analysis_store: AnalysisStore,
        schedule_store: ScheduleStore,
        runner: AnalysisRunner,
        tick_seconds: float = DEFAULT_TICK_SECONDS,
    ) -> None:
        self.analyses = analysis_store
        self.schedules = schedule_store
        self.runner = runner
        self.tick_seconds = tick_seconds
        self._stop = threading.Event()
        self._thread: Optional[threading.Thread] = None
        self._started_lock = threading.Lock()

    # ---- lifecycle ----------------------------------------------------

    def start(self) -> None:
        with self._started_lock:
            if self._thread is not None and self._thread.is_alive():
                return
            self._stop.clear()
            self._thread = threading.Thread(
                target=self._loop, name="schedule-runner", daemon=True
            )
            self._thread.start()

    def stop(self, *, timeout: float = 5.0) -> None:
        self._stop.set()
        if self._thread is not None:
            self._thread.join(timeout=timeout)

    def _loop(self) -> None:
        # Wait briefly before the first pass so an unrelated startup
        # error elsewhere doesn't get tangled up with a fire.
        if self._stop.wait(timeout=2.0):
            return
        while not self._stop.is_set():
            try:
                self.tick(datetime.now(timezone.utc))
            except Exception:  # noqa: BLE001
                logger.exception("Scheduler tick crashed")
            if self._stop.wait(timeout=self.tick_seconds):
                return

    # ---- one pass over all schedules ----------------------------------

    def tick(self, now_utc: datetime) -> None:
        """Public so tests (and a future ``/run-now-all`` admin
        endpoint) can drive a single pass deterministically."""
        for schedule in self.schedules.list():
            if not schedule.get("enabled", True):
                continue
            try:
                self._evaluate(schedule, now_utc)
            except Exception:  # noqa: BLE001
                logger.exception(
                    "Failed to evaluate schedule %s", schedule.get("id")
                )

    def _evaluate(self, schedule: Dict[str, Any], now_utc: datetime) -> None:
        kind = schedule.get("kind")
        if kind == "daily_after_close":
            self._evaluate_daily(schedule, now_utc)
        elif kind == "volatility_trigger":
            self._evaluate_volatility(schedule, now_utc)
        else:
            logger.warning(
                "Schedule %s has unknown kind %r; skipping",
                schedule.get("id"),
                kind,
            )

    def _evaluate_daily(
        self, schedule: Dict[str, Any], now_utc: datetime
    ) -> None:
        params = schedule.get("params") or {}
        if not should_fire_daily(
            now_utc=now_utc,
            last_run_at=schedule.get("last_run_at"),
            fire_hour_utc=int(params.get("fire_hour_utc", 21)),
            fire_minute_utc=int(params.get("fire_minute_utc", 30)),
            weekdays_only=bool(params.get("weekdays_only", True)),
        ):
            return
        self._fire(schedule, now_utc)

    def _evaluate_volatility(
        self, schedule: Dict[str, Any], now_utc: datetime
    ) -> None:
        params = schedule.get("params") or {}
        check_interval = int(params.get("check_interval_minutes", 15))
        throttle_h = int(params.get("throttle_hours", 6))
        threshold = float(params.get("threshold_pct", 1.5))

        # Pre-check the cheap throttles so we don't spend a yfinance
        # call when we already know we won't fire.
        last_check = _parse_iso(schedule.get("last_check_at"))
        if last_check is not None:
            mins = (now_utc - last_check).total_seconds() / 60.0
            if mins < check_interval:
                return
        last_fire = _parse_iso(schedule.get("last_run_at"))
        if last_fire is not None:
            hours = (now_utc - last_fire).total_seconds() / 3600.0
            if hours < throttle_h:
                # Still mark "checked" so we don't busy-loop on this
                # branch every minute.
                self.schedules.mark_checked(
                    schedule["id"], checked_at=now_utc.isoformat()
                )
                return

        intraday = fetch_intraday_return_pct(schedule["ticker"])
        # Always update last_check_at, even on a missing quote, so the
        # loop respects the polling cadence.
        self.schedules.mark_checked(
            schedule["id"], checked_at=now_utc.isoformat()
        )

        if not should_fire_volatility(
            now_utc=now_utc,
            last_run_at=schedule.get("last_run_at"),
            # Already updated above, but pass ``now`` so the pure
            # function also sees the fresh check time. The pre-check
            # above means this branch only runs when intervals have
            # elapsed, so this is a belt-and-braces guard.
            last_check_at=last_check.isoformat() if last_check else None,
            threshold_pct=threshold,
            throttle_hours=throttle_h,
            check_interval_minutes=check_interval,
            intraday_return_pct=intraday,
        ):
            return
        self._fire(schedule, now_utc)

    # ---- create + queue the analysis ---------------------------------

    def _fire(self, schedule: Dict[str, Any], now_utc: datetime) -> None:
        """Create an analysis from the schedule + push it to the runner."""
        ticker = schedule["ticker"]
        record = self.analyses.create(
            ticker=ticker,
            asset_type=schedule.get("asset_type") or _detect_asset_type(ticker),
            analysis_date=_today_iso(now_utc),
            language=schedule.get("language", "English"),
            max_debate_rounds=int(schedule.get("max_debate_rounds", 3)),
            max_risk_discuss_rounds=int(
                schedule.get("max_risk_discuss_rounds", 3)
            ),
        )
        self.runner.submit(record["id"])
        self.schedules.mark_fired(
            schedule["id"],
            analysis_id=record["id"],
            fired_at=now_utc.isoformat(),
        )
        logger.info(
            "Schedule %s fired (kind=%s ticker=%s analysis=%s)",
            schedule["id"],
            schedule.get("kind"),
            ticker,
            record["id"],
        )


# ---------------------------------------------------------------------------
# One-call helper to seed the default schedules a first-time user wants
# ---------------------------------------------------------------------------


RECOMMENDED_TICKER = "GLD"
RECOMMENDED_DEPTH = 3   # Medium = 3 rounds, matches CLI's middle option


def seed_recommended_schedules(
    schedule_store: ScheduleStore,
    *,
    ticker: str = RECOMMENDED_TICKER,
    language: str = "English",
) -> Dict[str, Dict[str, Any]]:
    """Idempotently create the two recommended schedules.

    Returns a dict ``{"daily": <record>, "volatility": <record>}``
    pointing at the schedules currently on disk for this ticker.
    Callers (the API) wire this behind an explicit user click — we
    don't seed silently on app boot, because that would surprise
    someone who uses a non-GLD ticker.
    """
    asset_type = _detect_asset_type(ticker)
    existing_by_kind: Dict[str, Dict[str, Any]] = {}
    for sched in schedule_store.list():
        if sched.get("ticker") == ticker.upper() and sched.get("kind") in (
            "daily_after_close",
            "volatility_trigger",
        ):
            existing_by_kind.setdefault(sched["kind"], sched)

    out: Dict[str, Dict[str, Any]] = {}
    if "daily_after_close" not in existing_by_kind:
        out["daily"] = schedule_store.create(
            ticker=ticker,
            asset_type=asset_type,
            kind="daily_after_close",
            language=language,
            max_debate_rounds=RECOMMENDED_DEPTH,
            max_risk_discuss_rounds=RECOMMENDED_DEPTH,
        )
    else:
        out["daily"] = existing_by_kind["daily_after_close"]

    if "volatility_trigger" not in existing_by_kind:
        out["volatility"] = schedule_store.create(
            ticker=ticker,
            asset_type=asset_type,
            kind="volatility_trigger",
            language=language,
            max_debate_rounds=RECOMMENDED_DEPTH,
            max_risk_discuss_rounds=RECOMMENDED_DEPTH,
        )
    else:
        out["volatility"] = existing_by_kind["volatility_trigger"]
    return out
