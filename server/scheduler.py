"""Background scheduler that fires recurring analyses.

A single worker thread wakes once a minute, walks every enabled
schedule in the store, and decides whether to enqueue a new analysis.
The decision logic is split into pure functions (``should_fire_*``)
so the test suite can drive them with frozen ``now`` / ``last_run_at``
values without involving threads or yfinance.

Intentional design choices:

  * One worker thread, not a pool. Schedule firing is cheap; the work
    itself is done by ``AnalysisRunner`` which is already serialised.
  * No persistent "next fire time" stored alongside the schedule —
    instead, every wake recomputes the answer from
    ``last_run_at`` + the current clock. Means a process restart never
    misses a daily fire and the on-disk schema stays minimal.
  * Volatility schedules call yfinance from the worker thread. We
    throttle via ``check_interval_minutes`` so a cluster of schedules
    can't hammer yahoo. Failures are logged and skipped — a missing
    quote shouldn't kill the daily schedule running next to it.
"""

from __future__ import annotations

import logging
import threading
from datetime import date, datetime, timedelta, timezone
from typing import Any, Dict, Optional

from .runner import AnalysisRunner
from .schedules import ScheduleStore
from .storage import AnalysisStore

logger = logging.getLogger(__name__)


def _detect_asset_type(ticker: str) -> str:
    """Local import to avoid the api↔scheduler circular dependency.

    ``detect_asset_type`` lives in ``server.api`` so the FastAPI layer
    can validate ticker inputs without pulling in scheduler code; we
    can't ``from .api import detect_asset_type`` at module level here
    because ``api`` imports ``BackgroundScheduler`` from this module.
    A function-level import is the simplest break in that cycle.
    """
    from .api import detect_asset_type as _impl

    return _impl(ticker)


# How often the worker thread wakes up. 60 s is granular enough for
# minute-precision daily schedules and infrequent enough that the
# loop is essentially free CPU-wise.
DEFAULT_TICK_SECONDS = 60.0


# ---------------------------------------------------------------------------
# Pure decision logic — no I/O, fully unit-testable
# ---------------------------------------------------------------------------


def _parse_iso(ts: Optional[str]) -> Optional[datetime]:
    """Tolerant ISO-8601 parser. Returns ``None`` for None/empty/invalid."""
    if not ts:
        return None
    try:
        # ``fromisoformat`` accepts our ``isoformat()`` output verbatim.
        d = datetime.fromisoformat(ts)
    except ValueError:
        return None
    # Treat naive timestamps as UTC — every persisted timestamp in
    # this codebase is generated by ``_utcnow_iso``, but we accept
    # legacy strings without the ``+00:00`` suffix to be safe.
    if d.tzinfo is None:
        d = d.replace(tzinfo=timezone.utc)
    return d


def should_fire_daily(
    *,
    now_utc: datetime,
    last_run_at: Optional[str],
    fire_hour_utc: int,
    fire_minute_utc: int,
    weekdays_only: bool = True,
) -> bool:
    """Decide whether a ``daily_after_close`` schedule should fire now.

    Fires at most once per day, on or after the configured target
    time. Crucially: if the worker misses the target minute (server
    sleep, restart, GC pause), the next tick after the target will
    still fire — we compare against the target *time*, not the
    instant we woke up.
    """
    if weekdays_only and now_utc.weekday() >= 5:  # Sat=5, Sun=6
        return False

    target = now_utc.replace(
        hour=fire_hour_utc, minute=fire_minute_utc, second=0, microsecond=0
    )
    if now_utc < target:
        return False

    last = _parse_iso(last_run_at)
    if last is None:
        return True
    # Fire when the most recent run pre-dates today's target time.
    # This naturally throttles to "once per day" without needing to
    # store a separate "fired today" flag.
    return last < target


def should_fire_volatility(
    *,
    now_utc: datetime,
    last_run_at: Optional[str],
    last_check_at: Optional[str],
    threshold_pct: float,
    throttle_hours: int,
    check_interval_minutes: int,
    intraday_return_pct: Optional[float],
) -> bool:
    """Decide whether a ``volatility_trigger`` schedule should fire.

    Three guards layered together so a single big move doesn't keep
    re-firing for hours:

      1. ``check_interval_minutes`` — how often we *poll* yfinance.
         Even a flat market can't trigger more than once per interval.
      2. ``throttle_hours`` — once we fire, the next fire is at least
         this many hours later, regardless of how big the move is.
      3. ``intraday_return_pct`` actually crossing the threshold.

    Returns False (without firing) when the price fetcher returns
    None — typically because yfinance is unreachable or the ticker
    has no quote yet.
    """
    # Poll-throttle. Skip price fetch entirely on subsequent ticks
    # within the polling window.
    last_check = _parse_iso(last_check_at)
    if last_check is not None:
        delta = (now_utc - last_check).total_seconds() / 60.0
        if delta < check_interval_minutes:
            return False

    # Fire-throttle. The poll above already burnt a yfinance call,
    # but firing is the expensive part (kicks an LLM run) so we
    # block on this hard.
    last_fire = _parse_iso(last_run_at)
    if last_fire is not None:
        delta_h = (now_utc - last_fire).total_seconds() / 3600.0
        if delta_h < throttle_hours:
            return False

    if intraday_return_pct is None:
        return False
    return abs(intraday_return_pct) >= threshold_pct


# ---------------------------------------------------------------------------
# Price fetcher (the only part that talks to the network)
# ---------------------------------------------------------------------------


def fetch_intraday_return_pct(ticker: str) -> Optional[float]:
    """Return ``(latest - prior_close) / prior_close * 100`` or None.

    Uses yfinance with a 2-day window so we always have access to
    yesterday's close even if today's session has just opened. If
    yfinance is unavailable or the ticker is unknown, returns None
    so the caller can skip the schedule cleanly.
    """
    try:
        import yfinance as yf
    except ImportError:
        logger.warning("yfinance not installed; volatility checks disabled")
        return None

    try:
        df = yf.download(
            ticker,
            period="2d",
            interval="1m",
            progress=False,
            auto_adjust=False,
            threads=False,
        )
    except Exception:  # noqa: BLE001 — third-party may raise anything
        logger.exception("yfinance intraday fetch failed for %s", ticker)
        return None

    if df is None or df.empty:
        return None
    try:
        col = df["Close"].dropna()
    except KeyError:
        return None
    if col.empty:
        return None

    # Group by trading-day date so we can pick yesterday's last close
    # vs today's most recent print without assuming any particular
    # number of intraday bars.
    by_date: Dict[date, float] = {}
    for ts, val in col.items():
        try:
            scalar = float(val)
        except (TypeError, ValueError):
            try:
                scalar = float(val.iloc[0])  # type: ignore[union-attr]
            except Exception:  # noqa: BLE001
                continue
        d = ts.date() if hasattr(ts, "date") else None
        if d is None:
            continue
        # Last entry per date wins — yfinance returns chronologically
        # ordered rows, so the final assignment is the latest print.
        by_date[d] = scalar

    if len(by_date) < 2:
        return None
    sorted_dates = sorted(by_date.keys())
    prior_close = by_date[sorted_dates[-2]]
    latest = by_date[sorted_dates[-1]]
    if prior_close == 0:
        return None
    return (latest - prior_close) / prior_close * 100.0


